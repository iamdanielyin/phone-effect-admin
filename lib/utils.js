const _ = require('lodash');

const app = exports = module.exports;

/**
 * 随机生成IP地址
 */
app.ip = function () {
    //ip范围
    const range = [[607649792, 608174079],//36.56.0.0-36.63.255.255
        [1038614528, 1039007743],//61.232.0.0-61.237.255.255
        [1783627776, 1784676351],//106.80.0.0-106.95.255.255
        [2035023872, 2035154943],//121.76.0.0-121.77.255.255
        [2078801920, 2079064063],//123.232.0.0-123.235.255.255
        [-1950089216, -1948778497],//139.196.0.0-139.215.255.255
        [-1425539072, -1425014785],//171.8.0.0-171.15.255.255
        [-1236271104, -1235419137],//182.80.0.0-182.92.255.255
        [-770113536, -768606209],//210.25.0.0-210.47.255.255
        [-569376768, -564133889], //222.16.0.0-222.95.255.255
    ];

    const index = _.random(0, 9);
    const ip = num2ip(range[index][0] + _.random(range[index][1] - range[index][0]));
    return ip;

};

/**
 * 将数字转换成IP
 * @param ip
 * @returns {string}
 */
function num2ip(ip) {
    const b = ['', '', '', ''];
    let x = "";
    b[0] = parseInt((ip >> 24) & 0xff);
    b[1] = parseInt((ip >> 16) & 0xff);
    b[2] = parseInt((ip >> 8) & 0xff);
    b[3] = parseInt(ip & 0xff);
    x = b[0] + "." + b[1] + "." + b[2] + "." + b[3];
    return x;
}
